#include <unistd.h>

char	*ft_strlowcase(char *str)
{
	int		c;

	c = 0;
	while (str[c] != '\0')
	{
		if (str[c] >= 'A' && 'Z' >= str[c])
		{
			str[c] = str[c] + 32;
		}
		c++;
	}
	return (str);
}
