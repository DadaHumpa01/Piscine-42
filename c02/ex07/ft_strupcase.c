#include <unistd.h>

char	*ft_strupcase(char *str)
{
	int c;

	c = 0;
	while (str[c] != '\0')
	{
		if (str[c] >= 'a' && 'z' >= str[c])
		{
			str[c] = str[c] - 32;
		}
		c++;
	}
	return (str);
}
