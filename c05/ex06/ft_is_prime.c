int		ft_is_prime(int nb)
{
	int i;
	int x;

	i = 2;
	if (nb == 0 || nb == 1 || nb < 0)
		return (0);
	while (i < nb)
	{
		x = nb / i;
		if (x * i == nb)
			return (0);
		i++;
	}
	return (1);
}
